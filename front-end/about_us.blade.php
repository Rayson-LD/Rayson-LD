<h2 class="display-4 animated fadeinleft" style="color:white;margin-bottom:5%;margin-top:5%;text-align:justify;font-size:200%;">Shray:The Store is a multi designer boutique located at Raghuvanshi Mills Compound, Lower Parel in Mumbai. Lower Parel is becoming a hub that houses most of Mumbai's high end boutiques for clothing, accessories, and home decor. It also has a number of new as well as upcoming commercial and residential buildings, restaurants and nightlife.</h2>
<h2 class="display-4 animated fadeinleft" style="color:white;margin-bottom:5%;text-align:justify;font-size:200%;">While its location is vital to the store, Shray boasts an innovative and fresh outlook to fashion through not only its location and decor (interiors done by one of India top interior designers, Rajiv Saini) but also through the vision of its owners Abha and Shreya Singhania. Abha and Shreya envision Shray as Mumbai's new fashion destination, housing established as well as emerging and gen-next designers from all over India. Their vision is to give clients customers a memorable experience when they enter our store, to awaken their senses and make them feel beautiful, fashion forward and glamorous.</h2>
<h2 class="display-4 animated fadeinleft" style="color:white;margin-bottom:5%;text-align:justify;font-size:200%;">Shray plans on executing its vision by housing exclusive collections not readily available with other boutiques in Mumbai, as well as hosting events and promotions. These events will serve the purpose of promoting the designers and showcasing their collections every season. Shray has a ready database of thousands of Mumbai elite from all walks of life including business, art, and glamour and promises to be the destination of Indian Fashion in Mumbai. Its clients will experience some of India's best talent and will have access to one of the vastest collections of silhouettes, thread work, and mix of both contemporary as well as traditional garments from the best of both established as well as emerging designers in Indian Fashion.</h2>
